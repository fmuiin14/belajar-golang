package main

// hrs didalem main

import "fmt"

// untuk mencetak tulisan

func main() {
	var name string

	name = "Fathul Muiin"
	fmt.Println(name)

	name = "Fathul Ganteng"
	fmt.Println(name)

	var umur = 20
	fmt.Println(umur)

	var bener = true
	fmt.Println(bener)

	// := sama dengan var
	kung := "Coba coba"
	fmt.Println(kung)

	var (
		namaDepan    = "Patul"
		namaBelakang = "Muiin"
	)
	fmt.Println(namaDepan)
	fmt.Println(namaBelakang)
}
